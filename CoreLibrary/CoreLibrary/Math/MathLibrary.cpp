#include "MathLibrary.h"

double MathLbrary::ArithMatic::Add(double a, double b)
{
	return a + b;
}

double MathLbrary::ArithMatic::Sub(double a, double b)
{
	return a - b;
}

double MathLbrary::ArithMatic::Multiply(double a, double b)
{
	return a * b;
}

double MathLbrary::ArithMatic::Divide(double a, double b)
{
	return a / b;
}
